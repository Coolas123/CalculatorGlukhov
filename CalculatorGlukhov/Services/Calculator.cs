namespace CalculatorGlukhov.Services
{
    public static class Calculator
    {
        public static double Multiply(double first, double second) => first * second;

        public static double Divide(double first, double second) => first / second;
        public static double Sum(double first, double second) => first + second;

        public static double Substract(double first, double second) => first - second;
    }
}
